class Task {
  final String id;
  final String clientId;
  final String title;
  final String description;
  final bool isCompleted;
  final DateTime dueDate;

  Task({
    required this.id,
    required this.clientId,
    required this.title,
    required this.description,
    this.isCompleted = false,
    required this.dueDate,
  });
}
